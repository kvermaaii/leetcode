class Solution {
    public long countCommas(long n) {
        if(n < 1000) return 0;
        
        if(n < 1_000_000L){ 
            return n - 999;
        } else if(n < 1_000_000_000L){ 
            return 2 * (n - 999_999) + 999_000;
        } else if(n < 1_000_000_000_000L){ 
            return 3 * (n - 999_999_999) + 2 * 999_000_000 + 999_000;
        } else if(n < 1_000_000_000_000_000L){ 
            return 4 * (n - 999_999_999_999L) + 3 * 999_000_000_000L + 2 * 999_000_000 + 999_000;
        } else {
            return 1 + 4 * (n - 999_999_999_999L) + 3 * 999_000_000_000L + 2 * 999_000_000 + 999_000;
        }
    }
}